// @tier: exclude
require('dotenv').config();
const { Pool } = require('pg');
const fs = require('fs');
const path = require('path');
const crypto = require('crypto');

const pool = new Pool({
  host: process.env.DB_HOST,
  port: process.env.DB_PORT,
  database: process.env.DB_NAME,
  user: process.env.DB_USER,
  password: process.env.DB_PASSWORD
});

/**
 * Export DISA STIG Checklists as .cklb (STIG Viewer 3 JSON format)
 *
 * Generates one .cklb file per STIG framework per organization, containing
 * the actual control implementation status from the database mapped to the
 * DISA status values (not_a_finding, open, not_reviewed, not_applicable).
 *
 * Output: controlweave/docs/wiki/security/reports/<org>/<stig-code>-<date>.cklb
 * Format: STIG Viewer 3.x CKLB (JSON)
 */

const WIKI_SECURITY_DIR = path.join(__dirname, '../../docs/wiki/security');
const WIKI_REPORTS_DIR = path.join(WIKI_SECURITY_DIR, 'reports');

const STIG_FRAMEWORKS = [
  { code: 'disa_stig_app',        display: 'Application Security and Development STIG' },
  { code: 'disa_stig_app_server', display: 'Application Server SRG' },
  { code: 'disa_stig_postgresql', display: 'PostgreSQL STIG' },
  { code: 'disa_stig_web_server', display: 'Web Server SRG' },
  { code: 'disa_stig_gpos',       display: 'General Purpose Operating System STIG' }
];

// ---------------------------------------------------------------------------
// Status mapping: ControlWeave → CKLB
// ---------------------------------------------------------------------------

function mapStatusToCklb(cwStatus) {
  switch (cwStatus) {
    case 'implemented':    return 'not_a_finding';
    case 'in_progress':    return 'open';
    case 'not_applicable': return 'not_applicable';
    case 'needs_review':   return 'not_reviewed';
    default:               return 'not_reviewed';
  }
}

// Map ControlWeave priority → CKLB severity
function mapPriorityToSeverity(priority) {
  switch (String(priority)) {
    case '1': return 'high';
    case '2': return 'medium';
    case '3': return 'low';
    default:  return 'medium';
  }
}

// ---------------------------------------------------------------------------
// CKLB generation
// ---------------------------------------------------------------------------

async function generateCklb(client, organizationId, orgName, frameworkCode) {
  // Fetch framework metadata
  const fwResult = await client.query(
    `SELECT id, name, version, description FROM frameworks WHERE code = $1 LIMIT 1`,
    [frameworkCode]
  );

  if (fwResult.rows.length === 0) {
    console.log(`  ⚠ Framework ${frameworkCode} not seeded — skipping`);
    return null;
  }

  const fw = fwResult.rows[0];

  // Fetch controls with implementation status
  const controlsResult = await client.query(
    `SELECT
       fc.control_id,
       fc.title,
       fc.description,
       fc.priority,
       fc.control_type,
       ci.status        AS impl_status,
       ci.implementation_narrative
     FROM framework_controls fc
     LEFT JOIN control_implementations ci
       ON fc.id = ci.control_id AND ci.organization_id = $1
     WHERE fc.framework_id = $2
     ORDER BY fc.control_id`,
    [organizationId, fw.id]
  );

  if (controlsResult.rows.length === 0) {
    console.log(`  ⚠ No controls found for ${frameworkCode} — skipping`);
    return null;
  }

  // Build rules array
  const rules = controlsResult.rows.map(ctrl => ({
    group_id:        ctrl.control_id,
    rule_version:    ctrl.control_id,
    severity:        mapPriorityToSeverity(ctrl.priority),
    status:          mapStatusToCklb(ctrl.impl_status),
    rule_title:      ctrl.title,
    discussion:      ctrl.description || '',
    check_content:   `Verify that ${ctrl.title.toLowerCase()} is implemented in accordance with ${fw.name}.`,
    fix_text:        `Implement ${ctrl.title.toLowerCase()} per ${fw.name} requirements.`,
    finding_details: ctrl.implementation_narrative || '',
    comments:        ctrl.impl_status
      ? `ControlWeave status: ${ctrl.impl_status} | Type: ${ctrl.control_type}`
      : `Not yet assessed | Type: ${ctrl.control_type}`,
    ccis:            [],
    srg_id:          ''
  }));

  // Assemble CKLB document
  const cklb = {
    title: `${fw.name} — ${orgName}`,
    id: crypto.randomUUID(),
    cklb_version: '1.0',
    target_data: {
      host_name:     orgName.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-+|-+$/g, '') || 'unknown',
      ip_address:    '',
      fqdn:          '',
      target_type:   'Computing',
      role:          'None',
      technology:    'ControlWeave GRC Platform',
      comments:      `Auto-generated by ControlWeave quarterly STIG export on ${new Date().toISOString()}`
    },
    stigs: [
      {
        stig_name:    fw.name,
        display_name: STIG_FRAMEWORKS.find(s => s.code === frameworkCode)?.display || fw.name,
        stig_id:      frameworkCode,
        version:      fw.version,
        release_info: `Release: ${fw.version} Benchmark Date: ${new Date().toISOString().split('T')[0]}`,
        rules
      }
    ]
  };

  return cklb;
}

// ---------------------------------------------------------------------------
// Main export
// ---------------------------------------------------------------------------

async function exportStigCklb() {
  const client = await pool.connect();

  try {
    console.log('='.repeat(80));
    console.log('DISA STIG CKLB EXPORT (STIG Viewer 3 format)');
    console.log('='.repeat(80));
    console.log();

    // Ensure output directories exist
    [WIKI_SECURITY_DIR, WIKI_REPORTS_DIR].forEach(dir => {
      if (!fs.existsSync(dir)) {
        fs.mkdirSync(dir, { recursive: true });
        console.log(`Created directory: ${dir}`);
      }
    });

    // Get all organizations
    const orgsResult = await client.query(
      'SELECT id, name FROM organizations ORDER BY created_at'
    );

    if (orgsResult.rows.length === 0) {
      console.log('No organizations found.');
      return { generated: 0, files: [] };
    }

    const timestamp = new Date().toISOString().split('T')[0];
    const generatedFiles = [];
    let totalGenerated = 0;

    for (const org of orgsResult.rows) {
      console.log(`\nOrganization: ${org.name}`);
      console.log('-'.repeat(60));

      const orgSlug = org.name.toLowerCase().replace(/[^a-z0-9]+/g, '-');
      const orgDir = path.join(WIKI_REPORTS_DIR, orgSlug);

      if (!fs.existsSync(orgDir)) {
        fs.mkdirSync(orgDir, { recursive: true });
      }

      for (const stig of STIG_FRAMEWORKS) {
        const cklb = await generateCklb(client, org.id, org.name, stig.code);
        if (!cklb) continue;

        const filename = `${stig.code}-${timestamp}.cklb`;
        const filepath = path.join(orgDir, filename);
        fs.writeFileSync(filepath, JSON.stringify(cklb, null, 2));

        const ruleCount = cklb.stigs[0].rules.length;
        const notAFinding = cklb.stigs[0].rules.filter(r => r.status === 'not_a_finding').length;
        const open = cklb.stigs[0].rules.filter(r => r.status === 'open').length;
        const notReviewed = cklb.stigs[0].rules.filter(r => r.status === 'not_reviewed').length;
        const notApplicable = cklb.stigs[0].rules.filter(r => r.status === 'not_applicable').length;

        console.log(`  ✓ ${filename}  (${ruleCount} rules: ${notAFinding} pass, ${open} open, ${notReviewed} not reviewed, ${notApplicable} N/A)`);
        generatedFiles.push(filepath);
        totalGenerated++;
      }
    }

    console.log();
    console.log('='.repeat(80));
    console.log(`EXPORT COMPLETE — ${totalGenerated} .cklb file(s) generated`);
    console.log(`Location: ${WIKI_REPORTS_DIR}`);
    console.log('='.repeat(80));

    return { generated: totalGenerated, files: generatedFiles };

  } catch (error) {
    console.error('Error exporting STIG CKLB files:', error);
    throw error;
  } finally {
    client.release();
    await pool.end();
  }
}

// Run the export
if (require.main === module) {
  exportStigCklb()
    .then(result => {
      console.log(`\n${result.generated} CKLB checklist(s) exported successfully.`);
      process.exit(0);
    })
    .catch(error => {
      console.error('CKLB export failed:', error);
      process.exit(1);
    });
}

module.exports = { exportStigCklb };
