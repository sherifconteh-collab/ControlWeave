#!/usr/bin/env node
// @tier: community
/**
 * Generates scripts/lib/frameworks/cmmc_2_0.js from a NIST SP 800-171 Rev 2
 * OSCAL catalog. CMMC 2.0 Level 2's 110 practices map 1:1 to the 110 SP
 * 800-171 Rev 2 requirements (CMMC 2.0 dropped the 20 CMMC-1.0-only "delta"
 * practices in the 2021 simplification) -- see
 * docs/FRAMEWORK_CATALOG_COMPLETION_PLAN.md Wave 1 for why Rev 2, not Rev 3,
 * is pinned as the CMMC derivation source even after Rev 3 becomes
 * ControlWeave's seeded/comprehensive 800-171 version.
 *
 * Source: https://github.com/FATHOM5CORP/oscal (CC0 -- freely redistributable,
 * no attribution required) -- content/SP800-171/oscal-content/catalogs/
 * NIST_SP-800-171_rev2_catalog.json. The underlying requirement text is
 * itself a public-domain US government work (NIST SP 800-171 Rev 2);
 * FATHOM5's CC0 dedication covers their specific OSCAL reformatting.
 *
 * Usage: node scripts/import-oscal-cmmc-l2.js path/to/NIST_SP-800-171_rev2_catalog.json
 */
const fs = require('fs');
const path = require('path');

// Standard CMMC 2.0 domain abbreviations, one per SP 800-171 family (3.x).
// Matches the abbreviations already used in the hand-picked subset this
// importer replaces (AC.L2-3.1.1, CA.L2-3.12.1, etc).
const DOMAIN_PREFIX = {
  '3.1': 'AC', '3.2': 'AT', '3.3': 'AU', '3.4': 'CM', '3.5': 'IA',
  '3.6': 'IR', '3.7': 'MA', '3.8': 'MP', '3.9': 'PS', '3.10': 'PE',
  '3.11': 'RA', '3.12': 'CA', '3.13': 'SC', '3.14': 'SI'
};

function familyOf(requirementId) {
  const parts = requirementId.split('.');
  if (parts.length < 2) {
    throw new Error(`Requirement id "${requirementId}" does not follow the expected "x.y.z" format`);
  }
  return `${parts[0]}.${parts[1]}`;
}

// The FATHOM5 source catalog has at least one control id with stray
// whitespace (" 3.2.1 ") from its CSV-to-OSCAL conversion -- trim
// defensively rather than let it silently fail domain-prefix lookup.
function cmmcId(requirementId) {
  if (typeof requirementId !== 'string' || !requirementId.trim()) {
    throw new Error(`Invalid or missing requirement id: ${requirementId}`);
  }
  const trimmedId = requirementId.trim();
  const family = familyOf(trimmedId);
  const prefix = DOMAIN_PREFIX[family];
  if (!prefix) throw new Error(`No CMMC domain prefix mapped for family ${family} (requirement ${trimmedId})`);
  return `${prefix}.L2-${trimmedId}`;
}

function convertCatalog(catalogPath) {
  const raw = JSON.parse(fs.readFileSync(catalogPath, 'utf8'));
  const catalog = raw && raw.catalog;
  if (!catalog) {
    throw new Error(`Invalid OSCAL catalog: no top-level "catalog" property in ${catalogPath}`);
  }

  const controls = [];
  const groups = Array.isArray(catalog.groups) ? catalog.groups : [];
  for (const group of groups) {
    const groupControls = Array.isArray(group.controls) ? group.controls : [];
    for (const control of groupControls) {
      const statement = (control.parts || []).find((p) => p.name === 'statement');
      const description = (statement && statement.prose) || control.title;
      controls.push({
        control_id: cmmcId(control.id),
        title: control.title,
        description,
        priority: '2',
        control_type: 'technical'
      });
    }
  }

  controls.sort((a, b) => a.control_id.localeCompare(b.control_id, undefined, { numeric: true }));
  const version = (catalog.metadata && catalog.metadata.version) || 'unknown';
  return { controls, version };
}

function writeModule(outPath, controls, version) {
  const header = `// Auto-generated by scripts/import-oscal-cmmc-l2.js from a NIST SP 800-171
// Rev 2 OSCAL catalog (FATHOM5CORP/oscal, CC0). CMMC 2.0 Level 2's 110
// practices map 1:1 to the 110 SP 800-171 Rev 2 requirements -- see
// docs/FRAMEWORK_CATALOG_COMPLETION_PLAN.md Wave 1 for why Rev 2 stays the
// pinned derivation source. Do not hand-edit -- re-run the importer instead.
// Source: https://github.com/FATHOM5CORP/oscal
//   content/SP800-171/oscal-content/catalogs/NIST_SP-800-171_rev2_catalog.json

const controls = ${JSON.stringify(controls, null, 2)};

module.exports = {
  framework: {
    code: 'cmmc_2.0',
    name: 'CMMC 2.0 (Level 2)',
    version: '2.0',
    description: 'Cybersecurity Maturity Model Certification Level 2 -- all 110 practices, aligned 1:1 with NIST SP 800-171 Rev 2, for DoD contractor CUI protection.',
    category: 'CUI Protection',
    tier_required: 'pro',
    coverage_status: 'comprehensive',
    source_catalog_version: ${JSON.stringify(version)}
  },
  controls,
  expectedCount: ${controls.length}
};
`;
  fs.writeFileSync(outPath, header);
}

function main() {
  const catalogPath = process.argv[2];
  if (!catalogPath) {
    console.error('Usage: node scripts/import-oscal-cmmc-l2.js <path-to-sp800-171-rev2-catalog.json>');
    process.exit(1);
  }
  const { controls, version } = convertCatalog(catalogPath);
  const outDir = path.join(__dirname, 'lib', 'frameworks');
  fs.mkdirSync(outDir, { recursive: true });
  const outPath = path.join(outDir, 'cmmc_2_0.js');
  writeModule(outPath, controls, version);
  console.log(`Wrote ${controls.length} CMMC 2.0 Level 2 practices (source SP 800-171 v${version}) to ${outPath}`);
}

main();
